public class StringArrayLength {

	public static void main(String[] args) {


		String[] a= new String[] {"Selenium","automation","Testleaf","Rezofy","monk","Mechies"};
		int lenght= a.length;

		if(lenght>5) {

			System.out.println("Given string of array element is greater than 5");

		}

		else {

			System.out.println("Given string of array element is lesser than 5");
		}


	}

}
